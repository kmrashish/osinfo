		<p>Mac OS is a series of graphical user interface-based operating systems developed by Apple Inc. for their Macintosh line of computer systems. </p><p>The original version was the integral and unnamed system software first introduced in 1984 with the original Macintosh, and referred to simply as the "System" software. The System was renamed to Mac OS in 1996 with version 7.6. The System is credited with popularizing the graphical user interface concept.</p><p>

Mac OS releases have existed in two major series. Up to major revision 9, from 1984 to 2000, it is historically known as Classic Mac OS. Major revision 10 (revisioned minorly, such as 10.0 through 10.9), from 2001 to present, has had the brand name of <strong>Mac OS X</strong> and now <strong>OS X</strong>. Both series share a general interface design and some shared application frameworks for compatibility, but also have deeply different architectures.</p></div>
